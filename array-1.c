/*
題目描述:
一維陣列反轉

輸入說明:
輸入一個一維陣列，元素最多不超過100個

輸出說明:
輸出反轉後的陣列，最後需有換行。

範例：

輸入範例:
4 6 3 69 234
56 89 23 3 1
176 5 890 643 2
0 500 6 634 55 123
87 77 32 22 111 4

輸出範例:
234 69 3 6 4
1 3 23 89 56
2 643 890 5 176
123 55 634 6 500 0
4 111 22 32 77 87
*/

#include <stdio.h>

int main()
{
    int n, i = 0;
    int list[100];

    while(scanf("%d", &n) != EOF)
    {
        list[i++] = n;
    }

    for(i-- ; i >= 0; i--)
    {
        printf("%d ", list[i]);
    }
    printf("\n");

    return 0;
}

